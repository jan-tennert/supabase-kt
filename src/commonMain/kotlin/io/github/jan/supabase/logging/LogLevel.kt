package io.github.jan.supabase.logging

/**
 * Represents a log level
 *
 */
enum class LogLevel {
    /**
     * Debug log level
     */
    DEBUG,

    /**
     * Info log level
     */
    INFO,

    /**
     * Warning log level
     */
    WARNING,
    /**
     * Error log level
     */
    ERROR,

    /**
     * No log level
     */
    NONE,
}